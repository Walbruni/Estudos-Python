
# Matrizes
# são vetor de vetores / array de arrays (ou seja, representação bidimensional de um vetor)
# usado em diversas aplicações como: computação gráfica, criptografia, tabelas e modelagem matemática
# representado por linhas e colunas
# o primeiro indice representa e a linha e o segundo indice representa a coluna


#       0      1      2      3

# 0     1      2      3      4

# 1     2      4      6      8

# 2     3      6      9      12

# 3     4      8      12     16


# Nessa matriz, o primeiro elemento matriz[0] é o vetor(1,2,3,4). O segundo elemento matriz[1] é o vetor (2,4,6,8), e assim por diante.

# Caso queira acessar o segundo elemento do terceiro vetor, deve escrever matriz[2][1], que corresponde ao elemento de valor 6.
